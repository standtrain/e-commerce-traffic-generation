// 获取当前语言
function getCurrentLang() {
    if (isset($_GET['lang']) && in_array($_GET['lang'], ['zh', 'en', 'ja', 'ko'])) {
        $_SESSION['lang'] = $_GET['lang'];
    }
    if (isset($_SESSION['lang'])) {
        return $_SESSION['lang'];
    }
    if (function_exists('getSetting')) {
        return getSetting('default_language', 'zh');
    }
    return 'zh';
}

// 翻译函数
function t($key, $replacements = []) {
    global $lang;
    $currentLang = getCurrentLang();
    $text = $lang[$currentLang][$key] ?? $lang['zh'][$key] ?? $key;
    foreach ($replacements as $k => $v) {
        $text = str_replace('{' . $k . '}', $v, $text);
    }
    return $text;
}

// 主题判断：返回 'dark' 或 'light'
function resolveThemeMode() {
    if (!function_exists('getSetting')) return 'dark';
    $mode = getSetting('theme_mode', 'dark');
    if ($mode === 'auto') {
        $start = intval(getSetting('auto_dark_start', 18));
        $end = intval(getSetting('auto_dark_end', 6));
        $hour = intval(date('G'));
        if ($start > $end) {
            return ($hour >= $start || $hour < $end) ? 'dark' : 'light';
        } else {
            return ($hour >= $start && $hour < $end) ? 'dark' : 'light';
        }
    }
    return $mode;
}

